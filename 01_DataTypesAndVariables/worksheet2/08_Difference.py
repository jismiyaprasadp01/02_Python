"""
What is the difference between None, 0, False, and "" in Python? Are they the same or different as variables? How does Python treat them in conditional statements?
"""
print(type(None))   # <class 'NoneType'>
print(type(0))      # <class 'int'>
print(type(False))  # <class 'bool'>
print(type(""))     # <class 'str'>
